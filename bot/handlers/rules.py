SECTIONS_MENU_VIEW = [(key, title) for key, title, _ in RULES_SECTIONS]
SECTIONS_BY_KEY = {key: (title, body) for key, title, body in RULES_SECTIONS}


def _save_prev_state(user):
    if user.state != AWAIT_RULES:
        sd = _j.loads(user.state_data or "{}")
        sd["_rules_prev_state"] = user.state
        user.state_data = _j.dumps(sd, ensure_ascii=False)


def _restore_prev_state(user):
    sd = _j.loads(user.state_data or "{}")
    prev_state = sd.pop("_rules_prev_state", None)
    user.state_data = _j.dumps(sd, ensure_ascii=False)
    return prev_state


def handle_rules(user, db, send_message):
    _save_prev_state(user)
    user.state = AWAIT_RULES
    db.commit()
    lines = ["📜 ПРАВИЛА ИГРЫ\n"]
    lines.append("Выбери раздел, чтобы узнать подробнее. "
                 "Можно нажать кнопку или написать номер раздела.\n")
    lines.append("\n".join(f"{i + 1}. {title}" for i, (key, title) in enumerate(SECTIONS_MENU_VIEW)))
    lines.append("\n0 или «Закрыть правила» — выйти из раздела.")
    send_message(
        "\n".join(lines),
        keyboard=rules_menu_keyboard(SECTIONS_MENU_VIEW),
    )


def handle_rules_section(user, db, send_message, section_key):
    entry = SECTIONS_BY_KEY.get(section_key)
    if not entry:
        handle_rules(user, db, send_message)
        return
    title, body = entry
    send_message(
        f"{body}\n\n— — —\nВыбери другой раздел кнопкой «◀ К правилам».",
        keyboard=rules_section_keyboard(),
    )


def handle_rules_pick(user, db, send_message, num):
    if 1 <= num <= len(RULES_SECTIONS):
        section_key = RULES_SECTIONS[num - 1][0]
        handle_rules_section(user, db, send_message, section_key)
    else:
        handle_rules(user, db, send_message)


def cancel_rules(user, db, send_message, upload_image=None):
    prev_state = _restore_prev_state(user)
    if prev_state and prev_state != AWAIT_RULES:
        user.state = prev_state
        db.commit()
        if prev_state.startswith("epic_egg_") or prev_state.startswith("epic_care_") or prev_state in ("await_epic_name", "await_epic_restart", "await_epic_egg_intro"):
            from bot.handlers.epic import handle_epic_command
            handle_epic_command(user, db, send_message, upload_image)
        else:
            send_message(
                "📜 Правила закрыты. Удачной вышивки!",
                keyboard=idle_keyboard(),
            )
    else:
        user.state = IDLE
        db.commit()
        send_message(
            "📜 Правила закрыты. Удачной вышивки!",
            keyboard=idle_keyboard(),
        )
